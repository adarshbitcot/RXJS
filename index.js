import { Observable } from 'rxjs';
const observable=new Observable((subscribe)=>{
    
    subscribe.complete(1)
    
    subscribe.error("hhh")
   
    subscribe.complete()
    
})

observable.subscribe({
    next:(value)=>{
      console.log("Next Value",value);
    },
    error:(err)=>{
     console.log("Error Occured",err);
    },
    complete:()=>{
         console.log("completed");
    }
})



